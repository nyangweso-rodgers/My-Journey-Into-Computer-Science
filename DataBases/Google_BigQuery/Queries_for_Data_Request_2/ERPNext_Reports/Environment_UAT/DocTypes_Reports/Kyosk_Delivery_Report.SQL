select 
    dn.posting_date,
    dn.posting_time,
    dn.lr_date,
    dn.customer,
    dn.customer_name,
    c.owner_phone_number,
    dn.duka_latitude,
    dn.duka_longitude,
    dn.territory,
    c.district,
    c.route_code,
    c.sale_district_code,
    dn.docstatus,
    dn.kyosk_delivery_note_status,
    dn.kyosk_status,
    dn.workflow_state,
    dn.kyosk_order_type,
    dn.driver,
    dn.vehicle_no,
    dn.driver_name,
    dn.name,
    dni.against_sales_order,
    dni.item_code,
    dni.item_name,
    dni.uom,
    dni.brand,
    dni.item_group,
    dni.original_quantity,
    dni.qty,
    dni.returned_qty,
    dni.price_list_rate,
    dni.base_price_list_rate,
    dni.discount_amount,
    dni.base_rate,
    dni.rate,
    dni.base_amount,
    dni.amount,
    dni.net_rate,
    dni.base_net_rate,
    dni.base_net_amount
    
from `tabDelivery Note` dn
left join `tabCustomer` c on dn.customer = c.name
left join `tabDelivery Note Item` dni on dn.name = dni.parent
where dn.posting_date >= '2022-02-07'